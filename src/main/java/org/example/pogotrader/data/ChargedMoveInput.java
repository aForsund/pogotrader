package org.example.pogotrader.data;

public class ChargedMoveInput {

  private String name;
  private String type;
  private String bars;
  private String cooldown;
  private String damageWindow;
  private String power;
  private String damage;
  private String energy;
  private String effect;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  public String getBars() {
    return bars;
  }

  public void setBars(String bars) {
    this.bars = bars;
  }

  public String getCooldown() {
    return cooldown;
  }

  public void setCooldown(String cooldown) {
    this.cooldown = cooldown;
  }

  public String getDamageWindow() {
    return damageWindow;
  }

  public void setDamageWindow(String damageWindow) {
    this.damageWindow = damageWindow;
  }

  public String getPower() {
    return power;
  }

  public void setPower(String power) {
    this.power = power;
  }

  public String getDamage() {
    return damage;
  }

  public void setDamage(String damage) {
    this.damage = damage;
  }

  public String getEnergy() {
    return energy;
  }

  public void setEnergy(String energy) {
    this.energy = energy;
  }

  public String getEffect() {
    return effect;
  }

  public void setEffect(String effect) {
    this.effect = effect;
  }

}

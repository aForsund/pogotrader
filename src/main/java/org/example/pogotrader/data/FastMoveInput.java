package org.example.pogotrader.data;

public class FastMoveInput {

  private String name;
  private String type;
  private String raidPower;
  private String raidEnergy;
  private String cooldown;
  private String power;
  private String energy;
  private String turns;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  public String getRaidPower() {
    return raidPower;
  }

  public void setRaidPower(String power) {
    this.raidPower = power;
  }

  public String getRaidEnergy() {
    return raidEnergy;
  }

  public void setRaidEnergy(String energy) {
    this.raidEnergy = energy;
  }

  public String getCooldown() {
    return cooldown;
  }

  public void setCooldown(String cooldown) {
    this.cooldown = cooldown;
  }

  public String getPower() {
    return power;
  }

  public void setPower(String power) {
    this.power = power;
  }

  public String getEnergy() {
    return energy;
  }

  public void setEnergy(String energy) {
    this.energy = energy;
  }

  public String getTurns() {
    return turns;
  }

  public void setTurns(String turns) {
    this.turns = turns;
  }

}
